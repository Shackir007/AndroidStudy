package com.example.mylearningappvolumecalc.model;

public class Shape {
    private int image;
    private String text;

    public Shape(int image, String text) {
        this.image = image;
        this.text = text;
    }


    public int getImage() {
        return image;
    }

    public String getText() {
        return text;
    }
}
